#!/usr/bin/env bash
# bin/audit-trend-pr-body.sh — emit the body for the nightly bot-opened
# audit-trend rollup PR. Stdout-only, no side effects.
#
# Called from .github/workflows/audit-trend.yml. Extracted to a script
# for the same reason as bin/changelog-pr-body.sh: the prior inline
# `gh pr create --body "$(cat <<'EOF' ... EOF)"` form put the heredoc
# body at column 1 inside a `run: |` YAML literal block scalar whose
# first content line is at column 11. Per the YAML 1.2 literal-style
# rule a less-indented line terminates the scalar, so PyYAML — and
# GitHub Actions' workflow registrar — rejected the file as malformed
# and every push to main produced 0-job failed runs.
#
# Refs-finding: F-002 (audit 20260514T132640Z).

set -euo pipefail

cat <<'BODY'
Automated rollup of audits/*.md by audit-trend.yml.

Regenerated by `bash plugins/agentify/lib/audit_aggregate.sh audits --trends`.

Reviewer checklist:
- [ ] Headline counts plausible.
- [ ] Recurring patterns (≥2 audits) make sense.
- [ ] No malformed audits silently dropped (check the workflow log).
BODY
